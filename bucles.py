print("Números del 1 al 5 multiplicados popr 2 con bucle for:")
for numero in range(1,6):
    print(numero * 2)

print("\nNúmeros del 1 al 5 multiplicados por 2 con bucle while:")
contador=1
while contador <= 5:
    print(contador * 2)
    contador += 1

#El while es ideal cuando no se sabe la cantidad de iteraciones necesarias antes de comenzar el bucle